package com.zhj.spring0725.bean;

public class User {
    private Integer userId;
    private String userNo;
    private String userName;
    private Integer userSex;
    private String userIdCard;
    private String userPhone;
    private String userPostbox;
    private String userAddress;
    private String userPlace;
    private String userNative;
    private String userEducation;
    private String userSchool;
    private String userBankId;
    private String userBank;
    private String userPassword;

    public User() {
    }

    public User(Integer userId, String userNo, String userName, Integer userSex, String userIdCard, String userPhone, String userPostbox, String userAddress, String userPlace, String userNative, String userEducation, String userSchool, String userBankId, String userBank, String userPassword) {
        this.userId = userId;
        this.userNo = userNo;
        this.userName = userName;
        this.userSex = userSex;
        this.userIdCard = userIdCard;
        this.userPhone = userPhone;
        this.userPostbox = userPostbox;
        this.userAddress = userAddress;
        this.userPlace = userPlace;
        this.userNative = userNative;
        this.userEducation = userEducation;
        this.userSchool = userSchool;
        this.userBankId = userBankId;
        this.userBank = userBank;
        this.userPassword = userPassword;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getUserNo() {
        return userNo;
    }

    public void setUserNo(String userNo) {
        this.userNo = userNo;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public Integer getUserSex() {
        return userSex;
    }

    public void setUserSex(Integer userSex) {
        this.userSex = userSex;
    }

    public String getUserIdCard() {
        return userIdCard;
    }

    public void setUserIdCard(String userIdCard) {
        this.userIdCard = userIdCard;
    }

    public String getUserPhone() {
        return userPhone;
    }

    public void setUserPhone(String userPhone) {
        this.userPhone = userPhone;
    }

    public String getUserPostbox() {
        return userPostbox;
    }

    public void setUserPostbox(String userPostbox) {
        this.userPostbox = userPostbox;
    }

    public String getUserAddress() {
        return userAddress;
    }

    public void setUserAddress(String userAddress) {
        this.userAddress = userAddress;
    }

    public String getUserPlace() {
        return userPlace;
    }

    public void setUserPlace(String userPlace) {
        this.userPlace = userPlace;
    }

    public String getUserNative() {
        return userNative;
    }

    public void setUserNative(String userNative) {
        this.userNative = userNative;
    }

    public String getUserEducation() {
        return userEducation;
    }

    public void setUserEducation(String userEducation) {
        this.userEducation = userEducation;
    }

    public String getUserSchool() {
        return userSchool;
    }

    public void setUserSchool(String userSchool) {
        this.userSchool = userSchool;
    }

    public String getUserBankId() {
        return userBankId;
    }

    public void setUserBankId(String userBankId) {
        this.userBankId = userBankId;
    }

    public String getUserBank() {
        return userBank;
    }

    public void setUserBank(String userBank) {
        this.userBank = userBank;
    }

    public String getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }
}
